    private void loginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loginActionPerformed
        // TODO add your handling code here:
        
         try{
            Connection c = connect.getConnect();//to establishes a database connection using the connect.getConnect() method
            Statement s = c.createStatement();//A statement object s is created using the connection.
            
            String sql = "SELECT * FROM login_patient WHERE p_Username='" + patientUsername.getText()
                    +"' and p_Pass='"+ patientPass.getText() + "'";
            ResultSet r = s.executeQuery(sql);//The query is executed using the executeQuery() method on the statement object s
            
            //If the query returns a result (using r.next()), it means that the login credentials match an entry in the login_patient table.
            if (r.next()){
                //a success message dialog is displayed
                JOptionPane.showMessageDialog(null, "Login Successfully !!");
                this.dispose();//Closed The current window
                 Patient_menu pMenu = new Patient_menu();//set a new instance of Patient_menu
                 pMenu.setVisible(true);//created and set to be visible.
            }else{
                //If the query does not return a result, it means the login credentials are incorrect. 
                //An error message dialog is displayed, and the focus is set on the patientPass field using patientPass.requestFocus().
                JOptionPane.showMessageDialog(this, "Wrong Username or Password !!");
                patientPass.requestFocus();
            }
        }catch (SQLException e){
            //Any SQL exceptions that occur during the process are caught in the catch block and a generic error message is printed.
            System.out.println("Error");
        }
    }//GEN-LAST:event_loginActionPerformed

