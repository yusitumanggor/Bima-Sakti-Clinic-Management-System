    private void showPassActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_showPassActionPerformed
        // TODO add your handling code here:
        
        //When the checkbox is selected, the patientPass field's echo character is set to 0, 
        //making the password characters visible in plain text. This allows the user to see the entered password.
          if(showPass.isSelected()){
            patientPass.setEchoChar((char)0);
        }
        else{
            //When the checkbox is deselected, the patientPass field's echo character is set to *, which hides the actual characters and displays asterisks (or another character) instead.
            patientPass.setEchoChar('*');
        }
    }//GEN-LAST:event_showPassActionPerformed

